      --Splits the string at a separator
      --Return: table of strings


        function split (inputstr, sep)
          if sep == nil then
                  sep = "%s"
          end
          local t={}
          for str in string.gmatch(inputstr, "([^"..sep.."]+)") do
                  table.insert(t, str)
          end
          return t
  end

